def build_analysis_prompt(raw_text: str) -> str:
    return f"""
请分析下面的项目资料，提炼成后续生成申请材料需要的结构化信息。

要求：
1. 提炼项目解决的核心痛点。
2. 提炼目标用户或使用场景。
3. 提炼 Agent/AI 的核心能力。
4. 提炼核心逻辑流。
5. 标出哪些内容是已有事实，哪些内容只是可规划方向。
6. 不要编造落地数据。

项目资料：
{raw_text}
""".strip()


def build_draft_prompt(raw_text: str, analysis: str) -> str:
    return f"""
请根据原始项目资料和项目分析，生成两部分内容。

第一部分：申请表描述
- 100 到 300 字。
- 适合填写到“请描述你使用 Agent 或 AI 驱动构建的具体成果”这一栏。
- 必须包含核心痛点、核心逻辑流、具体成果。
- 不要夸大，不要编造用户量、Token 消耗、企业落地等数据。

第二部分：完整项目报告
- 包含项目简介、核心痛点、核心逻辑流、技术亮点、适用场景、后续计划。
- 用简洁 Markdown 输出。

原始项目资料：
{raw_text}

项目分析：
{analysis}
""".strip()


def build_revision_prompt(draft: str) -> str:
    return f"""
请审查下面这份申请材料，并输出修订后的最终版本。

检查标准：
1. 申请表描述是否包含核心痛点。
2. 是否包含清晰的核心逻辑流。
3. 是否说明了 Agent 或 AI 的具体作用。
4. 是否存在夸大或虚假落地数据，如果有，请删除。
5. 申请表描述是否控制在 100 到 300 字。

输出格式：
【申请表描述】
修订后的申请表描述

【完整项目报告】
修订后的完整项目报告

待审查材料：
{draft}
""".strip()
