def build_report_prompt(raw_text: str) -> str:
    return f"""
请根据下面的项目资料，生成一段可以直接填写到申请表中的中文描述。

要求：
1. 重点描述使用 Agent 或 AI 驱动构建的具体成果。
2. 必须包含项目解决的核心痛点。
3. 必须包含核心逻辑流。
4. 如果合适，可以体现长链推理、多 Agent 协作、工具调用或自动化流程。
5. 文字真实可信，不要夸大，不要写虚假的落地数据。
6. 字数控制在 100 到 300 字。
7. 只输出最终描述，不要输出标题、解释或项目符号。

项目资料：
{raw_text}
""".strip()
